public class EstruturadeDadosArraysRegistros{
    public static void main(String[] args) {
        int vetor[] = {30, 45, 54, 55};

        for (int posicao = 0; posicao <= 3; posicao++){
            System.out.println(vetor[posicao]+ " ");
        }
    }
}


